function randomInteger(low, high)   
{   
    return low + Math.floor(Math.random() * (high - low));   
}   
  
  
function randomFloat(low, high)   
{   
    return low + Math.random() * (high - low);   
}   
  
function pixelValue(value)   
{   
    return value + '%';   
}    
  
function durationValue(value)   
{   
    return value + 's';   
}   
  
